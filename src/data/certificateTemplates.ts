export const templatesByCategory = {
  profesional: certificateTemplates.filter(t => t.categoria === 'profesional'),
  academico: certificateTemplates.filter(t => t.categoria === 'academico'),
  corporativo: certificateTemplates.filter(t => t.categoria === 'corporativo'),
  moderno: certificateTemplates.filter(t => t.categoria === 'moderno'),
  minimalista: certificateTemplates.filter(t => t.categoria === 'minimalista'),
};

// Agrupar por aspecto
export const templatesByAspect = {
  horizontal: certificateTemplates.filter(t => t.aspecto === 'horizontal'),
  cuadrado: certificateTemplates.filter(t => t.aspecto === 'cuadrado'),
};

// Obtener plantilla por ID
export function getTemplateById(id: string): CertificateTemplate | undefined {
  return certificateTemplates.find(t => t.id === id);
}

// Convertir plantilla a valores de formulario
export function templateToFormValues(template: CertificateTemplate) {
  return {
    color_fondo: template.color_fondo,
    color_borde: template.color_borde,
    color_texto_principal: template.color_texto_principal,
    color_texto_secundario: template.color_texto_secundario,
    color_acento: template.color_acento,
    mostrar_fecha: template.mostrar_fecha,
    mostrar_qr: template.mostrar_qr,
    mostrar_codigo: template.mostrar_codigo,
    // Campos adicionales para el layout
    template_id: template.id,
    aspecto: template.aspecto,
    logo_posicion: template.logo_posicion,
    logo_tamano: template.logo_tamano,
    firma_posicion: template.firma_posicion,
    borde_estilo: template.borde_estilo,
    borde_grosor: template.borde_grosor,
    fuente_titulo: template.fuente_titulo,
    fuente_cuerpo: template.fuente_cuerpo,
    mostrar_ornamentos: template.mostrar_ornamentos,
    mostrar_sello: template.mostrar_sello,
    sello_posicion: template.sello_posicion,
  };
}
